package Teoria;

public class Main {
    public static Integer sum(Integer a, Integer b) {
        if (a == null || b == null) {
            return null;
        }
        return a + b;
    }
    public Integer multiply(Integer a, Integer b) {
        if (a == null || b == null) {
            return null;
        }
        return a * b;
    }
}
